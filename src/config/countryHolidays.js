  function getCountries(){
    return Object.keys(DATA).map(function(code){
      return { code: code, name: DATA[code].name };
    }).sort(function(a, b){ return a.name.localeCompare(b.name); });
  }

  // Returns ISO date strings for the given country code.
  // If year is supplied, returns only that year. Otherwise returns all available years combined.
  function getHolidays(countryCode, year){
    var entry = DATA[countryCode];
    if(!entry) return [];
    if(year){
      return (entry.holidays[String(year)] || []).slice();
    }
    var all = [];
    Object.keys(entry.holidays).forEach(function(y){
      all = all.concat(entry.holidays[y]);
    });
    return all.sort();
  }

